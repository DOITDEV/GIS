package com.ahmap.domain;

public class MapServer {
	private String name;
	private String url;
	private String eleUrl;
	private String annoUrl;
	private String domUrl;
	private String annoDomUrl;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getEleUrl() {
		return eleUrl;
	}
	public void setEleUrl(String eleUrl) {
		this.eleUrl = eleUrl;
	}
	public String getAnnoUrl() {
		return annoUrl;
	}
	public void setAnnoUrl(String annoUrl) {
		this.annoUrl = annoUrl;
	}
	public String getDomUrl() {
		return domUrl;
	}
	public void setDomUrl(String domUrl) {
		this.domUrl = domUrl;
	}
	public String getAnnoDomUrl() {
		return annoDomUrl;
	}
	public void setAnnoDomUrl(String annoDomUrl) {
		this.annoDomUrl = annoDomUrl;
	}
}
